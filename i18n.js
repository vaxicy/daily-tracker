// ==================== i18n 工具函数 ====================
let currentLang = "zh";

function t(key, params = {}) {
  let text = (I18N[currentLang] && I18N[currentLang][key]) || key;
  Object.keys(params).forEach(k => {
    text = text.replace(`{${k}}`, params[k]);
  });
  return text;
}

function setLanguage(lang) {
  currentLang = lang;
  chrome.storage.local.set({ language: lang });
  applyI18n();
}

function loadLanguage(callback) {
  chrome.storage.local.get(["language"], (data) => {
    currentLang = data.language || "zh";
    if (callback) callback(currentLang);
  });
}

// ==================== 应用 i18n 到 DOM ====================
function applyI18n() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const text = t(key);
    if (el.tagName === "INPUT" && el.placeholder !== undefined) {
      el.placeholder = text;
    } else if (el.tagName === "TITLE") {
      document.title = text;
    } else {
      el.textContent = text;
    }
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.placeholder = t(key);
  });
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    const key = el.getAttribute("data-i18n-title");
    el.title = t(key);
  });
  document.querySelectorAll("[data-i18n-range]").forEach(el => {
    const key = el.getAttribute("data-i18n-range");
    el.setAttribute("data-range", t(key));
  });
  // 更新 HTML lang 属性
  document.documentElement.lang = currentLang === "en" ? "en" : "zh-CN";
}
